	.module pvclsound2
	

	; global from external entries / code
	.globl snd_areas

	; global from this module
	.globl _snd_stopall
	
	.area _CODE
    
;---------------------------------------------------------------------------------
; Here begin routines that can't be call from programs
;---------------------------------------------------------------------------------


;---------------------------------------------------------------------------------
; Here begin routines that can be call from programs
;---------------------------------------------------------------------------------
_snd_stopall:

	ld	b,#5
	ld	de,#0x000a
	ld	hl,#snd_areas
ssta1:
	ld	(hl),#0xff
	add	hl,de
	djnz	ssta1
	
	jp	0x1fd6
